    private void confirmarCambioButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_confirmarCambioButtonMouseClicked
        if (clubSelected == null)
            JOptionPane.showMessageDialog(null, "Selecciona un club");
        else
        {
            club.getGerente().getHistorico().add(clubSelected.getNombre());
            clubSelected.getGerente().borrarDatos();
            gerenteAux= clubSelected.getGerente();
            clubSelected.setGerente(club.getGerente());
            club.setGerente(gerenteAux);
            System.out.println("**********AÑADIDO***********");
            clubSelected.getGerente().verDatos();
            club.getGerente().verDatos();
            //exitoLabel.setVisible(true);
            this.setVisible(false);
            administracionClubs.setClubes(clubesList);
            administracionClubs.setLabels();
            administracionClubs.setVisible(true);
        }
    }//GEN-LAST:event_confirmarCambioButtonMouseClicked

